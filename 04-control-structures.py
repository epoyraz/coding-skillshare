# control structures
fruits = ["apple", "banana", "cherry"]
for fruit in fruits: # for each fruit in the list of fruits
    print(fruit)


for fruit in fruits: # for each fruit in the list of fruits
    x = fruit + " pie"
    print(x)

print("done")

# for loop
for i in range(10):
    print(i)

# while loop
i = 0
while i < 10:
    print(i)
    i = i + 1

# if statement
if (5 > 2):
    print("Five is greater than two!")

# if-else statement
if (5 > 2):
    print("Five is greater than two!")
else:
    print("Five is not greater than two!")

# if-elif-else statement
if (5 > 2):
    print("Five is greater than two!")
elif (5 == 2):
    print("Five is equal to two!")
else:
    print("Five is not greater than two!")
